import math

print(math.gcd(24,36)) # largest common divisor

# arrays fns
marks = [1, 2, 3, 4, 5, 5, 5, 5, 5, 5]
print("Sum of marks:", sum(marks))
print("Maximum mark:", max(marks))
print("Minimum mark:", min(marks))

# sqrt
print("Square root of 16:", math.sqrt(16))
# round
print("3.14159265359 rounded to 2 decimal places:", round(3.14159265359, 2))
# ceil
print("Ceiling of 3.1:", math.ceil(3.1))
# floor
print("Floor of 3.9:", math.floor(3.9))

# abs
print("Absolute value of -3:", abs(-3))